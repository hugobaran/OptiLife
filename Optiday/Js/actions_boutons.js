function ouvrir_popup(id){
    var popup=document.getElementById(id);
    popup.style.backgroundColor="rgba(0, 0, 0, 0.5)";
    popup.style.visibility="visible";
}

function fermer_popup(id){
    var popup = document.getElementById(id);
    popup.style.visibility="hidden";
}

